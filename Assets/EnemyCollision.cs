public class EnemyCollision : MonoBehaviour
{
    // Update is called once per frame
    void Update()
    {
        
    }

    private void OnCollisionEnter2D(Collision2D col)
    {
        if (col.gameObject.layer.Equals("Enemy"))
        {
            Destroy(gameObject);
        }
    }
}
